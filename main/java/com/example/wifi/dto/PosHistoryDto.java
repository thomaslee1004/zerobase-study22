package com.example.wifi.dto;


public class PosHistoryDto {
    String ID;
    String NAME;
    String SORT_VALUE;
    String REG_DT;
    String UDT_DT;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNAME() {
        return NAME;
    }

    public void setNAME(String NAME) {
        this.NAME = NAME;
    }

    public String getSORT_VALUE() {
        return SORT_VALUE;
    }

    public void setSORT_VALUE(String SORT_VALUE) {
        this.SORT_VALUE = SORT_VALUE;
    }

    public String getREG_DT() {
        return REG_DT;
    }

    public void setREG_DT(String REG_DT) {
        this.REG_DT = REG_DT;
    }

    public String getUDT_DT() {
        return UDT_DT;
    }

    public void setUDT_DT(String UDT_DT) {
        this.UDT_DT = UDT_DT;
    }
}


